class Menus:
    @staticmethod
    def has_account_menu():
        print('Do you have an account with us?')
        print('[1] Yes')
        print('[2] No')
        print('[3] Quit')


    @staticmethod
    def wants_to_login_menu():
        print('What would you like to do?')
        print('[1] Login')
        print('[2] Quit')

    @staticmethod
    def end_menu():
        print('What would you like to do?')
        print('[1] Look at playlist and mood history')
        print('[2] Logout')





